public class TheDifferentOperations
{
    public static void main(String[] args)
    {
        int operationA = -5 + 8 * 6;
        double operationB = (55+9) % 9;
        double operationC = 20 + -3*5 / 8;
        double operationD = 5 + 15 / 3 * 2 - 8 % 3;
        System.out.println("\nOperations: \nOperationA: " + operationA + "\n" + "OperationB: " + operationB + "\n" + "OperationC: " + operationC + "\n" + "OperationD: " + operationD);
    }
}